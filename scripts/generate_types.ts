// ---------------------------------------------------------------------------
// Write or check files
// ---------------------------------------------------------------------------

const checkMode = Deno.args.includes("--check");

const files: Record<string, string> = {
  "aai-sdk.d.ts": SDK_DTS,
  "aai-ui.d.ts": UI_DTS,
};

if (checkMode) {
  let ok = true;
  for (const [name, expected] of Object.entries(files)) {
    const path = join(OUT, name);
    let actual: string;
    try {
      actual = await Deno.readTextFile(path);
    } catch {
      console.error(`Missing: ${path}`);
      ok = false;
      continue;
    }
    if (actual !== expected) {
      console.error(`Out of date: ${path}`);
      ok = false;
    }
  }
  if (!ok) {
    console.error(
      "\nRun: deno run --allow-read --allow-write scripts/generate_types.ts",
    );
    Deno.exit(1);
  }
  console.log("Generated types are up to date");
} else {
  await Deno.mkdir(OUT, { recursive: true });
  for (const [name, content] of Object.entries(files)) {
    await Deno.writeTextFile(join(OUT, name), content);
  }
  console.log(`Generated type declarations in ${OUT}`);
}
