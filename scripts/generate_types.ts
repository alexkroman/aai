/**
 * Generate type declaration files for @aai/sdk and @aai/ui.
 *
 * Reads the actual source types and produces standalone .d.ts files
 * that are copied into scaffolded projects by `aai new`. This avoids
 * requiring `deno cache` for IDE autocomplete to work.
 *
 * Run: deno run --allow-read --allow-write scripts/generate_types.ts
 */

import { dirname, fromFileUrl, join } from "@std/path";

const ROOT = join(dirname(fromFileUrl(import.meta.url)), "..");
const OUT = join(ROOT, "cli", "_generated_types");

// ---------------------------------------------------------------------------
// @aai/sdk
// ---------------------------------------------------------------------------

